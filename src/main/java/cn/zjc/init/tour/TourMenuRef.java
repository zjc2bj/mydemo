package cn.zjc.init.tour;

public class TourMenuRef {
    private String crossKey;

    private String menuName;

    private int providerShow;

    private int distributorShow;

    public String getCrossKey() {
        return crossKey;
    }

    public String getMenuName() {
        return menuName;
    }

    public int getProviderShow() {
        return providerShow;
    }

    public int getDistributorShow() {
        return distributorShow;
    }

    public void setCrossKey(String crossKey) {
        this.crossKey = crossKey;
    }

    public void setMenuName(String menuName) {
        this.menuName = menuName;
    }

    public void setProviderShow(int providerShow) {
        this.providerShow = providerShow;
    }

    public void setDistributorShow(int distributorShow) {
        this.distributorShow = distributorShow;
    }

}
